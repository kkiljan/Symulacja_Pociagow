public class PołączenieMiędzyStacjami {

    double odległość;

    StacjaKolejowa stacjaPoczątkowa;
    StacjaKolejowa stacjaKońcowa;

    public PołączenieMiędzyStacjami(double odległość, StacjaKolejowa stacjaPoczątkowa, StacjaKolejowa stacjaKońcowa) {
        this.odległość = odległość;
        this.stacjaPoczątkowa = stacjaPoczątkowa;
        this.stacjaKońcowa = stacjaKońcowa;
    }

}
